/* ── Detection and Application ─────────────────────────────────────── */
let _landingLang = localStorage.getItem("landing_lang")
  || (navigator.language && navigator.language.startsWith("es") ? "es" : "en");

function getLandingLang() { return _landingLang; }

function setLandingLang(lang) {
  _landingLang = lang;
  localStorage.setItem("landing_lang", lang);
  applyLandingTranslations();
  // Restart chat demo with new language
  if (typeof switchTab === "function") {
    switchTab(window._chatActiveTab || "whatsapp");
  }
  // Re-render video idle if not playing
  if (typeof resetVideo === "function") {
    resetVideo();
  }
}

function tl(key) {
  if (_landingLang === "es") return null; // Use original HTML
  return (LANDING_I18N[_landingLang] && LANDING_I18N[_landingLang][key]) || null;
}

function applyLandingTranslations() {
  if (_landingLang === "es") {
    // Restore original Spanish from stored data
    document.querySelectorAll("[data-i18n-l]").forEach(function(el) {
      var orig = el.getAttribute("data-orig");
      if (orig !== null) {
        var mode = el.getAttribute("data-i18n-mode");
        if (mode === "html") el.innerHTML = orig;
        else el.textContent = orig;
      }
    });
    document.querySelectorAll("[data-i18n-l-placeholder]").forEach(function(el) {
      var orig = el.getAttribute("data-orig-ph");
      if (orig !== null) el.placeholder = orig;
    });
  } else {
    document.querySelectorAll("[data-i18n-l]").forEach(function(el) {
      var key = el.getAttribute("data-i18n-l");
      // Store original on first translation
      if (!el.hasAttribute("data-orig")) {
        var mode = el.getAttribute("data-i18n-mode");
        el.setAttribute("data-orig", mode === "html" ? el.innerHTML : el.textContent);
      }
      var val = tl(key);
      if (val) {
        var mode = el.getAttribute("data-i18n-mode");
        if (mode === "html") el.innerHTML = val;
        else el.textContent = val;
      }
    });
    document.querySelectorAll("[data-i18n-l-placeholder]").forEach(function(el) {
      if (!el.hasAttribute("data-orig-ph")) {
        el.setAttribute("data-orig-ph", el.placeholder);
      }
      var key = el.getAttribute("data-i18n-l-placeholder");
      var val = tl(key);
      if (val) el.placeholder = val;
    });
  }

  // Update toggle button
  var btn = document.getElementById("landingLangToggle");
  if (btn) btn.textContent = _landingLang === "es" ? "EN" : "ES";

  // Update html lang
  document.documentElement.lang = _landingLang;

  // Update meta tags
  if (_landingLang === "en") {
    document.title = "PropBot \u2014 Automate your real estate inquiries with AI";
    var metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.setAttribute("content", "Smart bot that answers sales and rental inquiries on WhatsApp, Facebook and Instagram reading your Google Sheet or Excel. Active 24/7, no code.");
  } else {
    document.title = "PropBot \u2014 Automatiz\u00e1 las consultas de tu inmobiliaria con IA";
    var metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.setAttribute("content", "Bot inteligente que responde consultas de ventas y alquileres por WhatsApp, Facebook e Instagram leyendo tu Google Sheet o Excel. Activo 24/7, sin c\u00f3digo.");
  }
}

document.addEventListener("DOMContentLoaded", function() {
  applyLandingTranslations();
});
